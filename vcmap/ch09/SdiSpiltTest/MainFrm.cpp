int CMainFrame::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
	if (CFrameWnd::OnCreate(lpCreateStruct) == -1)
		return -1;
	
	if (!m_wndToolBar.CreateEx(this, TBSTYLE_FLAT, WS_CHILD | WS_VISIBLE | CBRS_TOP
		| CBRS_GRIPPER | CBRS_TOOLTIPS | CBRS_FLYBY | CBRS_SIZE_DYNAMIC) ||
		!m_wndToolBar.LoadToolBar(IDR_MAINFRAME))
	{
		TRACE0("未能创建工具栏\n");
		return -1;      // 未能创建
	}

	if (!m_wndStatusBar.Create(this) ||
		!m_wndStatusBar.SetIndicators(indicators,
		  sizeof(indicators)/sizeof(UINT)))
	{
		TRACE0("未能创建状态栏\n");
		return -1;      // 未能创建
	}

	// TODO: 如果不需要可停靠工具栏，则删除这三行
	m_wndToolBar.EnableDocking(CBRS_ALIGN_ANY);
	EnableDocking(CBRS_ALIGN_ANY);
	DockControlBar(&m_wndToolBar);

	return 0;
}

BOOL CMainFrame::OnCreateClient(LPCREATESTRUCT /*lpcs*/,
	CCreateContext* pContext)
{
#define STATIC_SPLITTER

#ifdef STATIC_SPLITTER
	m_wndSplitter.CreateStatic(this, 2, 2);

	m_wndSplitter.CreateView(0, 0, RUNTIME_CLASS(CSdiSpiltTestView), CSize(0, 0), pContext);
	m_wndSplitter.CreateView(1, 1, RUNTIME_CLASS(CSdiSpiltTestView), CSize(0, 0), pContext);
	m_wndSplitter.CreateView(0, 1, RUNTIME_CLASS(CMyView), CSize(0, 0), pContext);
	m_wndSplitter.CreateView(1, 0, RUNTIME_CLASS(CMyView), CSize(0, 0), pContext);
#else
	pContext->m_pNewViewClass = RUNTIME_CLASS(CMyView);
	m_wndSplitter.Create(this, 2, 2, CSize(10, 10), pContext);
#endif

	return -1;
}

